package com.girnar.explorejaipur.jsondata;

public class JaipurBus {

	private String jaipurBusId;
	private String routeNo;
	private String startStand;
	private String endStand;
	private String intermidiate;
	private String color;
	private String radialCircular;

	public String getJaipurBusId() {
		return jaipurBusId;
	}

	public void setJaipurBusId(String jaipurBusId) {
		this.jaipurBusId = jaipurBusId;
	}

	public String getRouteNo() {
		return routeNo;
	}

	public void setRouteNo(String routeNo) {
		this.routeNo = routeNo;
	}

	public String getStartStand() {
		return startStand;
	}

	public void setStartStand(String startStand) {
		this.startStand = startStand;
	}

	public String getEndStand() {
		return endStand;
	}

	public void setEndStand(String endStand) {
		this.endStand = endStand;
	}

	public String getIntermidiate() {
		return intermidiate;
	}

	public void setIntermidiate(String intermidiate) {
		this.intermidiate = intermidiate;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getRadialCircular() {
		return radialCircular;
	}

	public void setRadialCircular(String radialCircular) {
		this.radialCircular = radialCircular;
	}

}
