package com.girnar.explorejaipur.jsondata;

/**
 * The Class UtilityData.
 */
public class Utility {


	private String utilityId;
	private String name;
	private String category;
	private String description;
	private String location;
	private String latitude;
	private String longitude;
	private String distance;
	private String distanceBusStand;

	/**
	 * Gets the category.
	 *
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}

	/**
	 * Sets the category.
	 *
	 * @param category the new category
	 */
	public void setCategory(String category) {
		this.category = category;
	}

	/**
	 * Gets the distance.
	 *
	 * @return the distance
	 */
	public String getDistance() {
		return distance;
	}

	/**
	 * Sets the distance.
	 *
	 * @param distance the new distance
	 */
	public void setDistance(String distance) {
		this.distance = distance;
	}

	/**
	 * Gets the latitude.
	 *
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}

	/**
	 * Sets the latitude.
	 *
	 * @param latitude the new latitude
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	/**
	 * Gets the longitude.
	 *
	 * @return the longitude
	 */
	public String getLongitude() {
		return longitude;
	}

	/**
	 * Sets the longitude.
	 *
	 * @param longitude the new longitude
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
	public String getUtilityId() {
		return utilityId;
	}

	public void setUtilityId(String utilityId) {
		this.utilityId = utilityId;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	/**
	 * @return the distanceBusStand
	 */
	public String getDistanceBusStand() {
		return distanceBusStand;
	}

	/**
	 * @param distanceBusStand the distanceBusStand to set
	 */
	public void setDistanceBusStand(String distanceBusStand) {
		this.distanceBusStand = distanceBusStand;
	}


}
